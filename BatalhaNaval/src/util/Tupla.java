package util;

public class Tupla
{
	int i,j;
	
	public Tupla(int i, int j)
	{
		this.i = i;
		this.j = j;
	}
	
	public Tupla()
	{
		
	}
	public int getI()
	{
		return i;
	}

	public void setI(int i)
	{
		this.i = i;
	}

	public int getJ()
	{
		return j;
	}

	public void setJ(int j)
	{
		this.j = j;
	}

	
}
